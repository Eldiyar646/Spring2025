package _02_java_operators;

public class ComparisonOperators {
    public static void main(String[] args) {

        int a, b;
        a = 15;
        b = 25;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);



    }
}
